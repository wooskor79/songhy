<h1>개인정보처리방침</h1>

<p>
SongHaYoung Gallery는 이용자의 개인정보를 중요하게 생각하며,
본 페이지를 통해 개인정보 처리 방침을 안내합니다.
</p>

<h2>1. 수집하는 개인정보</h2>
<p>
본 사이트는 회원가입 기능을 제공하지 않으며,
이용자의 개인정보를 직접적으로 수집하지 않습니다.
</p>

<h2>2. 쿠키(Cookie) 사용</h2>
<p>
본 사이트는 Google AdSense와 같은 제3자 광고 서비스를 사용할 수 있으며,
이 과정에서 쿠키가 사용될 수 있습니다.
</p>

<p>
Google을 포함한 제3자 광고 제공업체는
사용자의 관심사에 맞는 광고를 제공하기 위해
쿠키를 사용할 수 있습니다.
</p>

<h2>3. 광고 및 제3자 제공</h2>
<p>
Google AdSense는 DoubleClick 쿠키를 사용하여
사용자가 본 사이트 또는 다른 웹사이트를 방문한 기록을 기반으로
맞춤 광고를 제공할 수 있습니다.
</p>

<p>
사용자는 광고 설정을 통해
맞춤 광고 사용을 거부할 수 있습니다.
</p>

<h2>4. 개인정보 보호</h2>
<p>
본 사이트는 관련 법령을 준수하며,
개인정보 보호를 위해 합리적인 노력을 기울입니다.
</p>

